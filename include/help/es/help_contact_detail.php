<h1>Contactos</h1>
<p>
	Los contactos son las personas <b>asociadas</b> a esa empresa. Aquí es donde podemos definir, además del nombre de esa persona, su teléfono, dirección de correo, y por supuesto la empresa a la que pertenece.
</p>
<p>
	A veces no queremos meter las empresas de todos los contactos que tenemos porque son contactos “ocasionales”, p.e: contactos de una feria, o importados de un listado. Estos contactos se pueden “agrupar” bajo una cuenta de empresa “ficticia”, p.e: Campaña de XXXX. de forma, que si interesa realmente crear una cuenta de empresa para ese contacto, se puede crear y cambiar el contacto para que “cuelgue” de la nueva empresa.
</p>
<p>
	En la versión <b>Enterprise</b> existe control de acceso a los contactos de una empresa. Tienen acceso a ellos, el usuario asociado a la empresa y los usuarios que pertenecen a la empresa padre.
</p>
