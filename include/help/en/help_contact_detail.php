<h1>Contactos</h1>
<p>
	Contacts are people <b>related to the company.</b> Here is where we can define, apart from that person's name, his or her phone number, email address, and, of course, the company to which a contact belongs.
</p>
<p>
	Sometimes we don't want to include the companies corresponding to ALL our contacts, because of the fact that some contacts maybe considered “occasional”, i.e: expo contacts, or those imported from a list. These contacts can be “grouped” under a “false” company account, i.e: XXXXX Campaign. This way, if it's really interesting to create a new company account for a specific contact, it can be created and the contact changed, so that it “pends” on the new company.
</p>
<p>
	In the <b>Enterprise</b> version, there is access control to a company's contacts. Those who have access to them are: users associated to the company and those users belonging to the fathering company.
</p>
