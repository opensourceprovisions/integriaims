<h1>Empresas</h1>
<p>
	Son el elemento “central” de la gestión de cliente, almacenan datos sobre la empresa u organización. Esto es, el nombre, teléfono, el rol que tiene como empresa, y algunos otros datos. De especial relevancia es el grupo al que pertenece, porque solo los usuarios de integria que pertenezcan a ese grupo podrán ver los datos de esa compañía.
</p>
<h2>Roles de empresas</h2>
<p>
	Sirve para clasificar las compañías en categorías, como por ejemplo: partners, clientes, fabricantes, reseller, posible cliente, etc.
</p>
