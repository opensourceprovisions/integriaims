<h1>Creating an inventory item</h1>
<p>
	In order to create a new inventory item you'll only need to click the option “Create inventory item” from the left. You'll then see the following form with all the available options to configure that inventory item.
</p>
<p>
	<?php print_image("images/help/inventory7.png", false, false); ?>
</p>
<p>
	The most important fields on this form are: <b>owner, associated companies, associated users and viewers.</b> These fields define who sees the object. This way, the object will only be accessible by the owner, users directly related to an item or that belong to a related company. Furthermore, if the public flag is placed everyone will be able to see the item.
</p>
<p>
	An inventory item can have an established item “hierarchy” (one item can be a “child” to another). This is defined when deciding a “father”. Apart from this hierarchic relationship, other relationships amongst items can be established.
</p>
<p>
	Integria IMS' inventory has a simple stock control system. In order to manage an item stock, all items from an inventory have a “status” field which allows having this stock system. Possible statuses include: new, in use, unused, or unsubscribed. The signup and unsuscription dates can also be registered.
</p>