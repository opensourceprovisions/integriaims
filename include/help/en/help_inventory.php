<h1>Inventory searches</h1>
<p>
	Results of inventory searches can be seen in two ways: tree graph or list.
</p>
<p>
	In the tree graph view, inventory items are shown grouped by their corresponding types. Stock can also be seen, showing: total units, new units, unused units, minimum stock. If the number of total units is less than the minimum stock, it'll be marked in red.
</p>
<p>
	<?php print_image("images/help/inventory12.png", false, false); ?>
</p>
<p>
	In the list view information about the inventory is shown, along with its custom fields that have been marked to be shown.
</p>
<p>
	<?php print_image("images/help/inventory13.png", false, false); ?>
</p>
<p>
	<b>Fields that searches can be performed on are:</b>
	</br>
	</br>
	<ul>
		<li>Search (free text field): which refers to the name or inventory description. This is so if no custom fields are chosen. In this case, the search is limited to those fields' values too.</li>
		<li>Item type</li>
		<li>Custom fields in the item type</li>
		<li>Owner</li>
		<li>Contract</li>
		<li>Manufacturer</li>
		<li>Status</li>
		<li>Related user</li>
		<li>Related company</li>
		<li>Parent object</li>
	</ul>
</p>
<p>
	<?php print_image("images/help/inventory14.png", false, false); ?>
</p>
<p>
	Plus, an user can export search results to CSV and HTML formats in order to obtain lists that can be added to any type of report.
</p>