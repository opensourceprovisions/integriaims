<h1>Búsquedas de inventario</h1>
<p>
	El resultado de la búsqueda de inventario puede visualizarse en dos modos: árbol o listado.
</p>
<p>
	En la vista de árbol se muestran los objetos de inventario agrupados por tipo. También se visualiza el stock, mostrándose: Unidades totales:Nuevas:Sin usar:Stock mínimo. Si el número de unidades totales es inferior al stock mínimo, se marcará en rojo.
</p>
<p>
	<?php print_image("images/help/inventory12.png", false, false); ?>
</p>
<p>
	En la vista en modo listado se muestra información sobre el inventario, así como sus campos personalizados que hayan sido marcados para mostrar.
</p>
<p>
	<?php print_image("images/help/inventory13.png", false, false); ?>
</p>
<p>
	<b>Los campos por los que se pueden realizar búsquedas son:</b>
	</br>
	</br>
	<ul>
		<li>Buscar (campo de texto libre): que hace referencia al nombre o descripción del inventario. Esto es así si no se seleccionan campos personalizados. En este caso, la búsqueda acota también al valor de éstos campos.</li>
		<li>Tipo de objeto</li>
		<li>Campos personalizados del tipo de objeto</li>
		<li>Propietario</li>
		<li>Contrato</li>
		<li>Fabricante</li>
		<li>Estado</li>
		<li>Usuario asociado</li>
		<li>Compañía asociada</li>
		<li>Objeto padre</li>
	</ul>
</p>
<p>
	<?php print_image("images/help/inventory14.png", false, false); ?>
</p>
<p>
	Además puede exportar los resultados de las búsquedas en HTML y CSV para obtener listados que añadir a cualquier tipo de informe.
</p>