<h1>Operaciones sobre el ticket</h1>
<p>
	Una vez que ha seleccionado un ticket (o bien después de crearlo), podremos operar bien con las solapas superiores o las inferiores. Las solapas superiores dan acceso a la edición del ticket, o ver/añadir adjuntos o notas. También podemos volver a la búsqueda.
</p>
