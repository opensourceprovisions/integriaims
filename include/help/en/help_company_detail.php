<h1>Companies</h1>
<p>
	They're the “main” element in customer management. They store data on the enterprise or organization. This means: name, phone, role as a company, and other data. The group it belongs to is especially relevant because only Integria users that belong to that group can see that company's data.
</p>
<h2>Company roles</h2>
<p>
	Useful for classifying companies into categories, for example: partners, customers, manufacturers, resellers, possible customers, etc.
</p>
