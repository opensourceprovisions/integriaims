<h1>Proyectos Archivados</h1>

<p>Los proyectos pasan a archivados, guardando toda la información que contienen. Un proyecto archivado se puede borrar, y todas las unidades de trabajo que contiene se convierten en <b>“huérfanas”</b> y no están asignadas a ningún proyecto ni tarea, aunque siguen existiendo.
</br>
</br>
Para archivar un proyecto basta con darle al botón de Archivar de la vista Detalles de los proyectos. Automáticamente ese proyecto pasará a la vista de proyectos archivados. Dichos proyectos no notifican automáticamente ni el cierre de tarea, de proyecto o de hito (milestone). Dichos proyectos pueden ser activados de nuevo desde la vista de proyectos archivados.
</br>
</br>
Un proyecto archivado se puede borrar, pero esta operación debe ser hecha con mucho cuidado ya que <b>NO borra las unidades de trabajo asignadas a dicho proyecto</b>, pero sí la información de todas las tareas asociadas a este proyecto. Las unidades “huérfanas” constan, pero ya no se pueden relacionar con una tarea o proyecto concreto.</p>

