<h1>Creating a new task</h1>
<p>
	When it comes to defining a task, it's done within the 'start' and 'finish' time margin and has a priority assigned to it. Resources (people) are also assigned, along with target costs and time resources (in hours). This is task planning or estimation. From here on, and based on the amount of hours inputted to said task that the assigned users have applied, we'll be able to calculate time, cost and resource planning deviations. Integria automatically generates these indexes, which can be viewed in real time.
	</br>
	</br>
	<b>The first step is creating a task. We can't assign resources, add files, input WUs, etc. until a task isn't created. Once it has been created, the next logical step is to assign resources (people) to perform this task.</b>
