<h1>Import from CSV</h1>

<p>
A CSV file stores tabular data (numbers and text) in plain-text form. The columns are separated by commas:<br><br>
<b>id_usuario,password,real_name,email,telephone,description,avatar,disabled,id_company,num_employee,enable_login (without commas)</b><br><br>
Example file add_users.csv:<br><br>
user,pass_user,alberto,alberto@gmail.com,012345678,new user,people_1,0,3,222,1<br>
demo,pass_demo,maria,maria@gmail.com,876543210,new user,people_1,0,2,333,1<br>
</p>
