<h1>Autocompletado de UT (días)</h1>

<p>
Esta característica autocompletará las UT en los días no asignados para todos los usuarios excepto los listados en &laquo;Usuarios sin completado de UT&raquo;.
</p>
<p>
Días desde los que habrá autocompletado, hacia atrás y el mismo número de días, el número de horas de trabajo en caso de que no se introduzcan manualmente. Las horas introducidas no se asignan a ninguna tarea en ningún proyecto, sino a las horas <i>&laquo;Sin justificar&raquo;</i>. Por ejemplo, si establece 15 días, éstos se completarán con el número de <i>&laquo;Horas de trabajo por día&raquo;</i>, para todos los días en los que las horas introducidas sean inferiores a ése número, para los 15 días anteriores a 15 días antes de la fecha actual.
</p>
