<h1>Invoices</h1>
<p>
	Similarly to contracts, they serve the purpose of knowing when there are invoices that have been emitted but not charged, as well as knowing when and where a certain company has been charged. The system is used to manage EMITTED invoices by us, this means, in no case is this an ERP that can calculate treasury cycles, manage lack of payment, or other features common to ERPs. Invoice management, which includes invoice creation in PDF format; is oriented from the point of view of customer, not purveyor, management.
</p>
<p>
	In the Enterprise version there is access control to a company's invoices. The company owner and all the users with a Management (CM) profile have access to them. In the Open Source edition, everyone has access to invoices.
</p>
<p>
	<?php print_image("images/help/company7.png", false, false); ?>
</p>
<p>
	<b>Invoices include a variety of fields. The most important ones are:</b>
	<ul>
		<li><b>Invoice ID:</b> invoice number. It can't be duplicated on another invoice from the system. This system is strictly meant for invoices emitted from our company, therefore it would be impossible to have two invoices with the same ID.</li>
		<li><b>Reference:</b> generally meant to indicate an order number, a shopping order, or similar. Optional</li>
		<li><b>Invoice status:</b> payment pending, paid or cancelled.</li>
		<li><b>Creation date and payment date.</b></li>
		<li><b>Concept:</b> there are five lines with five amounts. For each concept there is an amount. Always in brute costs, without calculating taxes.</li>
		<li><b>Tax:</b> indicate the %, i.e. 21 for 21% taxes.</li>
		<li><b>Currency:</b> EUR is the default currency.</li>
		<li><b>Description:</b> text that will appear on the invoice, generally to clear up data from the order, add more data (for example the account number for payment, etc.).</li>
	</ul>
</p>
