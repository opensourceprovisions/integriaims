<h1>Hitos</h1>
<p>
	En general, un hito es una tarea de duración 0 que representa una fecha importante en un proyecto, como la finalización de una fase de proyecto, o la fecha en que un informe en particular debe ser entregado.
</p>
<p>
	Los hitos son una forma de conocer el avance del proyecto sin estar familiarizado con el proyecto y constituyen un trabajo de duración cero porque simbolizan un logro, un punto, un momento en el proyecto.
</p>
