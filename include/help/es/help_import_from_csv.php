<h1>Importar desde CSV</h1>

<p>
Un archivo CSV almacena datos tabulares (n&uacute;meros y texto) en formato de texto plano. Las columnas est&aacute;n separadas por comas:<br><br>
<b>id_usuario,password,real_name,email,telephone,description,avatar,disabled,id_company,num_employee,enable_login (sin comas)</b><br><br>
Archivo de ejemplo add_users.csv:<br><br>
user,pass_user,alberto,alberto@gmail.com,012345678,new user,people_1,0,3,222,1<br>
demo,pass_demo,maria,maria@gmail.com,876543210,new user,people_1,0,2,333,1<br>
</p>
