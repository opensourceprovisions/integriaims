<h1>Creación de nuevas unidades de trabajo (WU)</h1>
<p>
	La creación de una WU permite rellenar una serie de campos básicos (descripción, número de horas empleadas en la unidad de trabajo, perfil asignado, etc). Además de esos campos existen unos controles que permiten “dividir” la unidad de trabajo en bloques de X horas (por defecto 8 horas) y repartir ese tiempo en diferentes Unidades de Trabajo (WU) a lo largo del tiempo (para adelante o para detrás, según se elija) con el mismo contenido que la original, exceptuando por la fecha. Es la forma idónea de “rellenar” las horas para completar un calendario de vacaciones, por ejemplo.
	</br>
	</br>
	Las unidades de trabajo de un proyecto se pueden crear desde el menú contextual de la tarea donde se quiera añadir la unidad de trabajo (<b>Proyectos → Elegir proyecto → Elegir tarea → Agregar unidad de trabajo</b>), o se pueden hacer de forma general desde la vista de usuario (<b>Usuarios → Agregar horas de trabajo sueltas</b>). La única forma de agregar horas de trabajo en los tres supuestos especiales que contempla Integria IMS es desde la vista de usuario (<b>Agregar horas de trabajo sueltas</b>):
	</br>
	</br>
	<ul>
		<li>Vacaciones.</li>
		<li>Baja por enfermedad.</li>
		<li>No justificado.</li>
	</ul>
	</br>
	</br>
	Estos conceptos corresponden a tareas especiales dentro de un proyecto especial que se computa de manera diferente.
	</br>
	</br>
	Las unidades de trabajo se pueden crear de una en una o en bloques de forma que sea más cómodo y se pueden previsualizar todas antes de subirlas al sistema.
</p>
