<h1>Profiles</h1>

<p>
This feature allows you to define granular access to Integria IMS features.
</p>
<p>
<li>Incidents: Rights to manage incidents</li>
<li>Inventory: Rights to manage inventory</li>
<li>Agenda: Rights to manage the agenda</li>
<li>Projects: Rights to manage projects</li>
<li>Knowledge base: Rights to manage the Knowledge base</li>
<li>Todo: Rights to manage the Todos</li>
<li>Users: Rights to manage the users</li>
<li>Database: Rights to manage the database</li>
<li>Framework: Rights to manage Integira IMS itself</li>
</p>
<p>
To obtain more information check <a href="http://openideas.info/wiki/index.php?title=Integria:Documentation_es:Usuarios#Gestionar_perfiles">the full documentation</a>.
</p>
