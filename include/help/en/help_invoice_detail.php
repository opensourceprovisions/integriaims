<h2>Generating invoice IDs</h2>
<p>
	This utility allows us to automatically generate an ID whenever we create an invoice. For this we'll activate the option 'Enable auto ID' in the CRM configuration, as we can see in the screenshot below:
</p>
<p>
	<?php print_image("images/help/company10.png", false, false); ?>
</p>
<p>
	In the field named “Invoice ID Pattern” a string is save to later be used in generating the IDs. This pattern will contain both a fixed and a variable part. The variable part must be numeric and it will serve as a first element from which a sequence can be calculated. The variable parte will be placed in brackets. The rest will be constant for all invoices.
	Pattern example: 15/[1000]
	</br>
	In this case, the three first invoices that will be generated will be 15/1000, 15/1001 y 15/1002.
	</br>
	Invoice ID generation is only applied to those invoices that are “Sent”.
</p>
<h2>Invoice blocking</h2>
<p>
	An invoice can be blocked -using the lock icon- so that once it's blocked, it can't be modified. It can only be modified by the person who blocked the invoice.
</p>