<h1>Plantilla de macros</h1>

<p>
Las siguientes palabras ser&aacute;n reemplazadas por el valor real en las plantillas que las usen:
</p>
<p>
<li>_sitename_: Nombre de la p&aacute;gina, tal y como est&aacute; definido en la configuraci&oacute;n.
<li>_incident_title_: T&iacute;tulo del incidente.
<li>_username_: Nombre del usuario que va a recibir el email (nombre de usuario).</li>
<li>_fullname_: Nombre completo del usuario que va a recibir el email.</li>
<li>_incident_id: ID del incidente.
<li>_url_: URL del incidente.
<li>_creation_timestamp_: Fecha/Hora de la creaci&oacute;n del incidente.
<li>_update_timestamp_: &Uacute;ltima vez que el incidente fue actualizado.
<li>_owner_: Usuario que gestiona el incidente.
<li>_group_: Grupo asignado al incidente.
<li>_author_: Creador del incidente.
<li>_type_tickets_: Tipo de tickets.
<li>_priority_: Prioridad del incidente.
<li>_status_: Estado del incidente.
<li>_resolution_: Resoluci&oacute;n del incidente.
<li>_time_used_: Tiempo total usado en el incidente.
<li>_incident_main_text_: Descripci&oacute;n del incidente.
<li>_wu_user_: Usuario que a&ntilde;ade una unidad de trabajo.
<li>_wu_text_: Texto de la unidad de trabajo.
<li>_havecost_: Solo para reportes de unidades de trabajo de un proyecto.
<li>_taskname_: Solo para reportes de proyecto.
<li>_projectname_: Solo para reportes de proyecto. 
<li>Plantillas de campos personalizados: Esto permite que al crear un tipo de objeto el nombre de los campos que agregas puedes incluirlos como una macro la cual mostrara el valor de dicho campo: _nombre del campo personalizado_.</li>


</p>
