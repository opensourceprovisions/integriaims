<h1>Búsqueda de tickets personalizadas</h1>

<p>
Un ticket es un concepto genérico, como la palabra "ticket", pero esto se puede aplicar a un fallo de software (un bug), a un problema de comunicaciones, o a un problema de un cliente con un pedido. En un caso tendrá una serie de campos "personalizados", y en otros otra. Por ello, la posibilidad de definir búsquedas de tickets personalizadas permite esta flexibilidad. En el mismo sistema podemos trabajar con diferentes tipos de búsquedas.

Todos los campos personalizados se pueden buscar, y guardar en una búsqueda personalizada, al seleccionar el tipo de ticket en la búsqueda.<br><br>

<p>
<?php print_image("images/help/custom_type_search.png", false); ?>
</p>

<br><br>

Sobre un ticket ya creado podemos cambiar su tipo, opcionalmente, ya que existe una casilla en el setup para definir este comportamiento. Si lo hacemos, perderemos el valor que contenían los campos personalizados del tipo anterior.

A continuación vemos la creación de búsquedas personalizadas con un ejemplo:<br>

<p>
<?php print_image("images/help/custom_search_example.png", false); ?>
</p>

<br><br>

Al hacer click sobre el botón Custom Search se despliega un formulario donde podemos crear una búsqueda o cargar una ya creada.
En este ejemplo, guardaremos la búsqueda de incidencias en estado No cerrado del grupo Engineering. Una vez seleccionados estos parámetros, le pondremos un nombre descriptivo a ésta búsqueda y pulsaremos el botón Guardar.
