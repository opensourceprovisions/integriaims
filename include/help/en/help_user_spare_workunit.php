<h1>Creating new WorkUnits (WUs)</h1>
<p>
	Creating a WU allows an user to fill in a series of basic fields (description, number of hours applied to the WU, assigned profile, etc.). Apart from these fields there is a series of controls that allow “dividing” the WU into block of X amount of hours (default is 8 hours) and distribute this time among different WUs along time (chosen to be forward or backward in that time span) with the same content as the original, except for the date. This is the idilic way to “fill out” hours to complete, for example, a vacations calendar.
	</br>
	</br>
	WorkUnits for a project can be created from the context menu on the task we wish to add the WU to (<b>Projects→Choose Project→Choose Task→ Add WorkUnit</b>), It can also be done from the user's general view (<b>Users→Add Spare WorkUnit</b>). The only way to add work hours in all three special ways that Integria IMS contemplates, is from the user view (<b>Add Spare WorkUnits</b>):
	</br>
	</br>
	<ul>
		<li>Vacations.</li>
		<li>Sick days.</li>
		<li>Unexcused nonattendance.</li>
	</ul>
	</br>
	</br>
	These concepts correspond to special tasks within a special project witch is computed differently.
	</br>
	</br>
	WUs can be created one by one or in blocks, to make it more comfortable. They can also be previewed before being inputted to the system.
</p>
